package it.polito.oop.elective;

import java.util.LinkedList;
import java.util.List;

public class Student {
	private String id;
	private double avgVote;
	private List<Course> enroll=new LinkedList<>();
	private Course corso;
	
	public Student(String id, double avgVote) {
		super();
		this.id = id;
		this.avgVote = avgVote;
	}
	/**
	 * @return the id
	 */
	public String getId() {
		return id;
	}
	/**
	 * @return the avgVote
	 */
	public double getAvgVote() {
		return avgVote;
	}
	/**
	 * @param id the id to set
	 */
	public void setId(String id) {
		this.id = id;
	}
	/**
	 * @param avgVote the avgVote to set
	 */
	public void setAvgVote(double avgVote) {
		this.avgVote = avgVote;
	}
	
	public void setEnroll(List<Course> corsi) {
		this.enroll=corsi;
	}
	
	public List<Course> getEnroll() {
		return enroll;
	}
	
	public void setCorso(Course c) {
		corso=c;
	}
	public Course getCorso() {
		return corso;
	}
	
}
