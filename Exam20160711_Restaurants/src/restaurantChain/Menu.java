package restaurantChain;

public class Menu {
	private String name;
	private Double price;
	
	public Menu(String name, Double price) {
		super();
		this.name = name;
		this.price = price;
	}

	/**
	 * @return the name
	 */
	public String getName() {
		return name;
	}

	/**
	 * @return the price
	 */
	public Double getPrice() {
		return price;
	}

}
