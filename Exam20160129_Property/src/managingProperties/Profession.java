package managingProperties;

import java.util.LinkedList;
import java.util.List;

public class Profession {
	private String name;
	private List<String> professionista=new LinkedList<>();
	
	public Profession(String name, List<String> professionista) {
		super();
		this.name = name;
		this.professionista = professionista;
	}
	/**
	 * @return the name
	 */
	public String getName() {
		return name;
	}
	/**
	 * @return the professionista
	 */
	public List<String> getProfessionista() {
		return professionista;
	}
	
}
